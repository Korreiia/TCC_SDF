<h1>História</h1>
        <pre>A Etec possui um sistema de lixo eletrônico, que os computadores que recebe de doação são desmanufaturados no laboratório de hardware
e as peças são analisadas, testadas e por fim são utilizadas para montar novos computadores para doação a alunos e a população
e essa solicitação é feita na secretaria da escola seguida de uma lista.
A doa tec foi criado para que essas peças de hardware seja cadastrada tendo controle de modelos, quantidade, e sendo possível alunos e a população
solicitar um computador pelo site para receber a doação através desse projeto.</pre>
